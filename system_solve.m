function [H]=system_solve(X1,Y1,X2,Y2)
 
    A=[ X1(1) Y1(1) 1 0 0 0 -X1(1)*X2(1) -Y1(1)*X2(1);
        0 0 0 X1(1) Y1(1) 1 -X1(1)*Y2(1) -Y1(1)*Y2(1);
        X1(2) Y1(2) 1 0 0 0 -X1(2)*X2(2) -Y1(2)*X2(2);
        0 0 0 X1(2) Y1(2) 1 -X1(2)*Y2(2) -Y1(2)*Y2(2);
        X1(3) Y1(3) 1 0 0 0 -X1(3)*X2(3) -Y1(3)*X2(3);
        0 0 0 X1(3) Y1(3) 1 -X1(3)*Y2(3) -Y1(3)*Y2(3);
        X1(4) Y1(4) 1 0 0 0 -X1(4)*X2(4) -Y1(4)*X2(4);
        0 0 0 X1(4) Y1(4) 1 -X1(4)*Y2(4) -Y1(4)*Y2(4)];
 
    B=[X2(1);Y2(1);X2(2);Y2(2);X2(3);Y2(3);X2(4);Y2(4)];
 
    X=A\B;
 
    H=[ X(1) X(2) X(3);
        X(4) X(5) X(6);
        X(7) X(8)    1];
end